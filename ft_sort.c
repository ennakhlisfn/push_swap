void	ft_sort(int *sor, int j)
{
	int	i;
	int	k;
	int	tmp;

	i = 0;
	while (i < j - 1)
	{
		k = i + 1;
		while (k < j)
		{
			if (sor[i] > sor[k])
			{
				tmp = sor[i];
				sor[i] = sor[k];
				sor[k] = tmp;
			}
			k++;
		}
		i++;
	}
}
