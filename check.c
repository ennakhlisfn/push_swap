#include "libft.h"

int	check(char **arv, int arc)
{
	int	i;

	i = 0;
	while (i < arc)
	{
		if (!ft_strlen(arv[i]) || string_check(arv[i]))
			return (1);
		i++;
	}
	return (0);
}
