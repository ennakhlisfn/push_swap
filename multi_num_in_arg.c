int	multi_num_in_arg(char *arv)
{
	int	i;

	i = 0;
	while (arv[i] == ' ' || arv[i] == '+' || arv[i] == '-')
		i++;
	while (arv[i] >= '0' && arv[i] <= '9')
		i++;
	while (arv[i] == ' ' || arv[i] == '+' || arv[i] == '-')
		i++;
	if (arv[i] >= '0' && arv[i] <= '9')
		return (1);
	return (0);
}
