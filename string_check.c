int	string_check(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if ((s[i] < '0' || s[i] > '9') && s[i] != ' ')
		{
			if (s[i] != '+' && s[i] != '-')
				return (1);
			else if (s[i] == '+' || s[i] == '-' )
			{
				if (s[i + 1] == ' ' || s[i + 1] == 0)
					return (1);
				if (i != 0 && s[i - 1] != ' ')
					return (1);
			}
		}
		i++;
	}
	return (0);
}
